// lib
var _ = require('../lib/utils');


/* -----------------------------------------------------------------------------
 * task
 * ---------------------------------------------------------------------------*/

module.exports = function (grunt) {

  /* ---------------------------------------------------------------------------
   * load
   * -------------------------------------------------------------------------*/

  grunt.loadTasks(_.taskpath('grunt-contrib-jshint'));


  /* ---------------------------------------------------------------------------
   * config
   * -------------------------------------------------------------------------*/

  grunt.config('jshint', {
    options: {
      jshintrc: '.jshintrc',
      force: true
    },
    src: [
      'Gruntfile.js',
      'src/**/*.js',
      'test/**/*.js'
    ]
  });

};