var _ = require('../lib/utils');


/* -----------------------------------------------------------------------------
 * task
 * ---------------------------------------------------------------------------*/

module.exports = function (grunt) {

  /* ---------------------------------------------------------------------------
   * load
   * -------------------------------------------------------------------------*/

  grunt.loadTasks(_.taskpath('grunt-contrib-watch'));


  /* ---------------------------------------------------------------------------
   * config
   * -------------------------------------------------------------------------*/

  grunt.config('watch', {
    options: {
      spawn: true,
      livereload: true
    },
    grunt: {
      files: ['Gruntfile.js'],
      tasks: ['build']
    },
    tests: {
      files: ['test/**/*.*']
    },
    js: {
      files: ['src/**/*.js'],
      tasks: ['build']
    }
  });

};